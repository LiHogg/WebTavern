def _build_extra_demo_venue(
    *,
    key,
    owner,
    legal_entity,
    manager_keys,
    name,
    slug,
    city,
    district,
    address,
    lat,
    lng,
    cuisine,
    price_category,
    venue_theme,
    preset,
    short_description,
    description,
    rating,
    deposit_amount,
    requires_manager_confirmation=False,
    allow_shared_seating=True,
):
    table_specs = [
        ("T1", 2, 90, 100),
        ("T2", 4, 280, 100),
        ("T3", 4, 480, 170),
        ("T4", 6, 730, 230),
        ("T5", 4, 280, 350),
        ("T6", 2, 700, 470),
    ]
    tables = [
        {
            "key": f"{key}_t{index}",
            "name": label,
            "seats_count": seats,
            "is_combinable": seats <= 4,
            "x": x,
            "y": y,
        }
        for index, (label, seats, x, y) in enumerate(table_specs, start=1)
    ]
    return {
        "key": key,
        "owner": owner,
        "legal_entity": legal_entity,
        "name": name,
        "slug": slug,
        "country": "Россия",
        "city": city,
        "district": district,
        "address": address,
        "latitude": lat,
        "longitude": lng,
        "cuisine": cuisine,
        "price_category": price_category,
        "venue_theme": venue_theme,
        "short_description": short_description,
        "description": description,
        "status": "active",
        "average_rating": rating,
        "is_published": True,
        "branding": _branding(preset),
        "booking_rule": {
            "default_duration_minutes": 60,
            "slot_step_minutes": 10,
            "cleanup_buffer_minutes": 20,
            "payment_hold_minutes": 30,
            "requires_manager_confirmation": requires_manager_confirmation,
            "allow_client_approximate_time": True,
            "allow_table_combination": True,
            "allow_shared_seating": allow_shared_seating,
            "deposit_amount": deposit_amount,
            "deposit_currency": "RUB",
        },
        "manager_keys": manager_keys,
        "halls": [
            {
                "key": f"{key}_hall",
                "name": "Основной зал",
                "description": "Демо-зал для каталога, выбора стола и проверки геофильтров.",
                "capacity": sum(table[1] for table in table_specs),
                "sort_order": 1,
                "layout": {"canvas_width": 1200, "canvas_height": 800},
                "decor_items": _decor_items(),
                "tables": tables,
            }
        ],
    }


