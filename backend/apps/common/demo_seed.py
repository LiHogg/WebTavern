def _branding(preset, custom=None):
    data = PRESET_THEME_BASE.get(preset, PRESET_THEME_BASE["northern_blue"]).copy()
    data["theme_preset"] = preset
    data["use_custom_palette"] = False
    data["contrast_warning"] = False
    if custom:
        data.update(custom)
        data["use_custom_palette"] = True
    return data


