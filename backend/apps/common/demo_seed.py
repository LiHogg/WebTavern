# поэтому ниже используются демонстрационные схемы посадки для проверки бронирования.


def _real_demo_tables(prefix, variant="classic"):
    variants = {
        "classic": [
            ("T1", 2, 90, 110, 110, 110), ("T2", 2, 250, 110, 110, 110),
            ("T3", 4, 430, 150, 130, 120), ("T4", 4, 650, 150, 130, 120),
            ("T5", 6, 850, 260, 160, 130), ("T6", 4, 440, 380, 130, 120),
        ],
        "long": [
            ("W1", 2, 90, 95, 105, 105), ("W2", 2, 90, 250, 105, 105),
            ("C1", 4, 330, 120, 130, 120), ("C2", 4, 530, 120, 130, 120),
            ("C3", 6, 760, 190, 165, 130), ("B1", 2, 940, 420, 105, 105),
        ],
        "panorama": [
            ("P1", 2, 130, 90, 110, 110), ("P2", 2, 300, 90, 110, 110),
            ("P3", 4, 500, 105, 130, 120), ("P4", 4, 710, 120, 130, 120),
            ("S1", 6, 250, 340, 165, 130), ("S2", 6, 570, 360, 165, 130),
        ],
        "compact": [
            ("A1", 2, 90, 120, 105, 105), ("A2", 2, 230, 120, 105, 105),
            ("A3", 4, 390, 130, 125, 115), ("A4", 4, 560, 130, 125, 115),
            ("A5", 4, 390, 330, 125, 115), ("A6", 2, 700, 360, 105, 105),
        ],
    }
    return [
        {
            "key": f"{prefix}_{index}",
            "name": label,
            "seats_count": seats,
            "is_combinable": seats <= 4,
            "x": x,
            "y": y,
            "width": width,
            "height": height,
        }
        for index, (label, seats, x, y, width, height) in enumerate(variants.get(variant, variants["classic"]), start=1)
    ]


def _real_demo_decor(variant="classic"):
    base = [
        {"item_type": "entrance", "label": "Вход", "x": 36, "y": 688, "width": 150, "height": 42},
        {"item_type": "bar", "label": "Бар", "x": 890, "y": 70, "width": 220, "height": 78},
        {"item_type": "plant", "label": "Зелень", "x": 1030, "y": 610, "width": 80, "height": 80},
    ]
    if variant == "panorama":
        base.append({"item_type": "window", "label": "Панорамные окна", "x": 180, "y": 20, "width": 660, "height": 34})
    elif variant == "long":
        base.append({"item_type": "sofa", "label": "Диванная линия", "x": 60, "y": 520, "width": 390, "height": 70})
    elif variant == "compact":
        base.append({"item_type": "label", "label": "Касса / хостес", "x": 760, "y": 70, "width": 190, "height": 60})
    else:
        base.append({"item_type": "window", "label": "Окна", "x": 260, "y": 24, "width": 430, "height": 34})
    return base


